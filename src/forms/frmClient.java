    private void tblClientMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClientMouseClicked
        int row = tblClient.getSelectedRow();
        int ba = tblClient.convertRowIndexToModel(row);
        int tblClick = Integer.valueOf(tblClient.getModel().getValueAt(ba, 0).toString());
        String tblClickCompany = tblClient.getModel().getValueAt(ba, 1).toString();
        String fetchData = "select * from tblClientList where cl_id=? and cl_company=?";
        try{
            pstmt = conn.prepareStatement(fetchData);
            pstmt.setInt(1, tblClick);
            pstmt.setString(2,tblClickCompany);
            rs = pstmt.executeQuery();
            if (rs.next()){
                txtClientAddress.setText(rs.getString(4));
                txtClientCompany.setText(rs.getString(2));
                txtClientContact.setText(rs.getString(5));
                txtClientEmail.setText(rs.getString(6));
                txtClientName.setText(rs.getString(3));
                getSelectedID = rs.getInt(1);
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_tblClientMouseClicked

