    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        getNextID();
        String insertQuery = "INSERT INTO tblClientList(cl_id,cl_company,cl_contactperson,cl_address,cl_contact,cl_email)"
        + " values (?,?,?,?,?,?)";
        String editQuery = "UPDATE tblClientList set cl_contactperson=?,cl_address=?,cl_contact=?,cl_email=? where cl_id=? and cl_company=? and cl_contactperson=?";
        if (add==true  && edit==false){
            try{
                pstmt = conn.prepareStatement(insertQuery);
                pstmt.setInt(1,getMaxClientID);
                pstmt.setString(2, txtClientCompany.getText());
                pstmt.setString(3, txtClientName.getText());
                pstmt.setString(4, txtClientAddress.getText());
                pstmt.setString(5, txtClientContact.getText());
                pstmt.setString(6, txtClientEmail.getText());       
                pstmt.execute();
                pstmt.close();
                JOptionPane.showMessageDialog(this, "CLIENT ADDED");
                fillTable();
                btnAdd.setEnabled(true);
                btnEdit.setEnabled(true);
            }catch(SQLException e){
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }else if(edit==true && add==false){
            try{
                pstmt = conn.prepareStatement(editQuery);
                pstmt.setString(1, txtClientName.getText());
                pstmt.setString(2, txtClientAddress.getText());
                pstmt.setString(3, txtClientContact.getText());
                pstmt.setString(4, txtClientEmail.getText()); 
                pstmt.setInt(5,getSelectedID);
                pstmt.setString(6, txtClientCompany.getText());
                pstmt.setString(7, txtClientName.getText());
                pstmt.executeUpdate();
                pstmt.close();
                JOptionPane.showMessageDialog(this, "CLIENT DETAILS UPDATED");
                fillTable();
                btnAdd.setEnabled(true);
                btnEdit.setEnabled(true);
            }catch(SQLException e){
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

