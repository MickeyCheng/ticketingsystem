    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        add = true;
        edit = false;
        btnEdit.setEnabled(false);
        enableTexts();
        clearTexts();
        getNextCID();
        lblID.setText(String.valueOf(getMaxCID));
        txtClientName.requestFocus();
    }//GEN-LAST:event_btnAddActionPerformed
    private void getNextCID(){
        try{
            pstmt = conn.prepareStatement("select cd_cid from tblClientDetails order by cd_cid DESC LIMIT 1");
            rs = pstmt.executeQuery();
            if (rs.next()){
                getMaxCID = rs.getInt("cd_cid");
                getMaxCID++;
            }else{
                getMaxCID = 1;
            }
        }catch(SQLException e){
            e.getMessage();
        }
    }
    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
       if (txtClientName.getText().equals("")){
           JOptionPane.showMessageDialog(this, "PLEASE TYPE CLIENT NAME","CLIENT DETAILS", JOptionPane.WARNING_MESSAGE);
       }else{
        if (add==true && edit==false){
            String insertClientDetails = "INSERT INTO tblClientDetails (cd_cid,cd_name,cd_address,"
                    + "cd_poc,cid_email,cd_contact) values (?,?,?,?,?,?)";
            try{
                pstmt = conn.prepareStatement(insertClientDetails);
                pstmt.setString(1,lblID.getText());
                pstmt.setString(2,txtClientName.getText());
                pstmt.setString(3,txtClientAddress.getText());
                pstmt.setString(4,txtClientPOC.getText());
                pstmt.setString(5,txtClientEmail.getText());
                pstmt.setString(6,txtContact.getText());
                pstmt.execute();
                pstmt.close();
                JOptionPane.showMessageDialog(this, "CLIENT DETAILS SAVED");
                clearTexts();
                disableTexts();
                btnEdit.setEnabled(true);
                fillClientTable();
            }catch(SQLException e){
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }else if (add==false && edit== true){
            String editClientDetails = "UPDATE tblClientDetails set cd_name=?,cd_address=?,"
                    + "cd_poc=?,cid_email=?,cd_contact=? where cd_cid=?";
            try{
                pstmt = conn.prepareStatement(editClientDetails);
                pstmt.setString(1,txtClientName.getText());
                pstmt.setString(2,txtClientAddress.getText());
                pstmt.setString(3,txtClientPOC.getText());
                pstmt.setString(4,txtClientEmail.getText());
                pstmt.setString(5,txtContact.getText());
                pstmt.setString(6,lblID.getText());
                pstmt.execute();
                pstmt.close();
                JOptionPane.showMessageDialog(this, "CLIENT DETAILS UPDATED");
                clearTexts();
                disableTexts();
                btnAdd.setEnabled(true);
                fillClientTable();
            }catch(SQLException e){
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }
       }
    }//GEN-LAST:event_btnSaveActionPerformed

