    private void fillClientTable(){
        try{
            pstmt = conn.prepareStatement("SELECT cd_cid,cd_name,cd_poc,cid_email,cd_contact "
                    + "from tblClientDetails order by cd_name");
            rs = pstmt.executeQuery();
            tblClient.setModel(DbUtils.resultSetToTableModel(rs));
            pstmt.close();
            tblClient.getColumnModel().getColumn(0).setHeaderValue("ID");
            tblClient.getColumnModel().getColumn(1).setHeaderValue("NAME");
            tblClient.getColumnModel().getColumn(2).setHeaderValue("POC");
            tblClient.getColumnModel().getColumn(3).setHeaderValue("EMAIL");
            tblClient.getColumnModel().getColumn(4).setHeaderValue("CONTACT");
        }catch(SQLException e){
            e.getMessage();
        }
    }
