    private void tblClientMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClientMouseClicked
        int row = tblClient.getSelectedRow();
        int ba = tblClient.convertRowIndexToModel(row);
            String tblClick = tblClient.getModel().getValueAt(ba, 0).toString();
            String fetchData = "Select * from tblClientDetails where cd_cid=?";
        try{
            pstmt = conn.prepareStatement(fetchData);
            pstmt.setString(1, tblClick);
            rs = pstmt.executeQuery();
            if (rs.next()){
                lblID.setText(String.valueOf(rs.getInt("cd_cid")));
                txtClientName.setText(rs.getString("cd_name"));
                txtClientAddress.setText(rs.getString("cd_address"));
                txtClientPOC.setText(rs.getString("cd_poc"));
                txtClientEmail.setText(rs.getString("cid_email"));
                txtContact.setText(rs.getString("cd_contact"));
            }
            pstmt.close();
        }catch(SQLException e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_tblClientMouseClicked

