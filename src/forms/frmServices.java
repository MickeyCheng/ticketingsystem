    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String insertProductQuery = "INSERT INTO tblServiceList(sl_id,sl_name,sl_price)"
        + " values (?,?,?)";
        String editProductQuery = "UPDATE tblServiceList set sl_name=?,sl_price=? where sl_id=?";
        if (add==true  && edit==false){
            try{
                pstmt = conn.prepareStatement(insertProductQuery);
                pstmt.setInt(1, getMaxID);
                pstmt.setString(2,txtServiceName.getText() );
                pstmt.setString(3, df.format(Double.valueOf(txtPrice.getText())));
                pstmt.execute();
                pstmt.close();
                JOptionPane.showMessageDialog(this, "SERVICE SAVED");
                txtPrice.setText("");
                txtServiceName.setText("");
                lblServiceID.setText("");
                fillTable();
                btnAdd.setEnabled(true);
                btnEdit.setEnabled(true);
                disableTexts();
            }catch(SQLException e){
                e.getMessage();
            }
        }else if(edit==true && add==false){
            try{
                pstmt = conn.prepareStatement(editProductQuery);
                pstmt.setString(1,txtServiceName.getText());
                pstmt.setString(2,df.format(Double.valueOf(txtPrice.getText())));
                pstmt.setInt(3, Integer.valueOf(lblServiceID.getText()));
                pstmt.executeUpdate();
                pstmt.close();
                JOptionPane.showMessageDialog(this, "SERVICE DETAILS UPDATED");
                txtPrice.setText("");
                txtServiceName.setText("");
                lblServiceID.setText("");
                fillTable();
                btnAdd.setEnabled(true);
                btnEdit.setEnabled(true);
                disableTexts();
            }catch(SQLException e){
                e.getMessage();
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

