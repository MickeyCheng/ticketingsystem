    private void tblServiceMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblServiceMouseClicked
        int row = tblService.getSelectedRow();
        int ba = tblService.convertRowIndexToModel(row);
        String tblClick = tblService.getModel().getValueAt(ba, 0).toString();
        String fetchData = "SELECT * from tblServiceList where sl_id=?";
        try{
            pstmt = conn.prepareStatement(fetchData);
            pstmt.setString(1,tblClick);
            rs = pstmt.executeQuery();
            if (rs.next()){
                lblServiceID.setText(rs.getString("sl_id"));
                txtPrice.setText(rs.getString("sl_price"));
                txtServiceName.setText(rs.getString("sl_name"));
            }
            pstmt.close();
        }catch(SQLException e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_tblServiceMouseClicked

