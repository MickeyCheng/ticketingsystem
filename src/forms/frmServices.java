    private void fillTable(){
        try{
            pstmt = conn.prepareStatement("SELECT * from tblServiceList order by sl_id");
            rs = pstmt.executeQuery();
            tblService.setModel(DbUtils.resultSetToTableModel(rs));
            tblService.getColumnModel().getColumn(0).setHeaderValue("ID");
            tblService.getColumnModel().getColumn(1).setHeaderValue("SERVICE");
            tblService.getColumnModel().getColumn(2).setHeaderValue("PRICE");
        }catch(SQLException e){
            e.getMessage();
        }
    }
    private void getNextID(){
        try{
            pstmt = conn.prepareStatement("Select * from tblServiceList order by sl_id DESC LIMIT 1");
            rs = pstmt.executeQuery();
            if (rs.next()){
                getMaxID = rs.getInt(1);
                getMaxID++;
            }else{
                getMaxID = 1;
            }
            lblServiceID.setText(String.valueOf(getMaxID));
            pstmt.close();
        }catch(SQLException e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

